m = int(input())

if m <= 2:
    print("NO")
    exit()

print("YES")
w = [[(1, 1), (2, 2), (1, 3), (2, 1), (1, 2), (2, 3)],
     [(1, 1), (2, 2), (1, 4), (2, 3), (1, 2), (2, 1), (1, 3), (2, 4)],
     [(1, 1), (2, 2), (1, 4), (2, 5), (1, 3), (2, 1), (1, 2), (2, 3), (1, 5), (2, 4)]]

a = m // 3
n = m % 3
for i in range(a-1):
    for v in w[0]:
        print(v[0], v[1] + 3 * i)

for v in w[n]:
    print(v[0], v[1] + m - (n+3))
